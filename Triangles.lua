local function HexToC(text)
	if (string.len(text) < 6)
		CONS_Printf(server, "Invalid hexadecimal value: " .. text)
		return
	end
	local hex = string.lower(text)
	local values = {}
	local byte
	for i = 0, 5
		byte = string.byte(hex, i+1, i+1)
		if(byte >= 48 and byte <= 57) // 0 - 9
			values[i] = byte - 48
		elseif(byte >= 97 and byte <= 102) // a - f
			values[i] = byte - 87
		else
			CONS_Printf(server, "Invalid hexadecimal value: " .. text)
			return
		end
	end
	// Converting the data to a fixed number between 0 and 1
	local r = (values[0]*16 + values[1]) * 256
	local g = (values[2]*16 + values[3]) * 256
	local b = (values[4]*16 + values[5]) * 256
//	print(r .. " " .. g .. " " .. b)
	return Vec3(r,g,b)
end

