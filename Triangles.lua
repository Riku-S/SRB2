	local x0f = x0*FRACUNIT
	local y0f = y0*FRACUNIT
	local x1f = x1*FRACUNIT
	local y1f = y1*FRACUNIT
	local x2f = x2*FRACUNIT
	local y2f = y2*FRACUNIT

	if(wireframe)
		local slope
		local x
		local y
		if(x0 == x1)
			v.drawFill(x0,min(y0,y1),1,abs(y0-y1),c)
		elseif(y0 == y1)
			v.drawFill(min(x0,x1),y0,abs(x0-x1),1,c)
		else
			slope = FixedDiv(y0f-y1f,x0f-x1f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y0,y1)
				local maxy = max(y0,y1)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y0*256 + FixedMul(slope,x0*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x0,x1)
				local maxx = max(x0,x1)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x0*256 , slope)/256 + y0
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		if(x1 == x2)
			v.drawFill(x0,min(y1,y2),1,abs(y1-y2),c)
		elseif(y1 == y2)
			v.drawFill(min(x1,x2),y1,abs(x1-x2),1,c)
		else
			slope = FixedDiv(y1f-y2f,x1f-x2f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y1,y2)
				local maxy = max(y1,y2)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y1*256 + FixedMul(slope,x1*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x1,x2)
				local maxx = max(x1,x2)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x1*256 , slope)/256 + y1
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		if(x2 == x0)
			v.drawFill(x2,min(y2,y0),1,abs(y2-y0),c)
		elseif(y2 == y0)
			v.drawFill(min(x2,x0),y2,abs(x2-x0),1,c)
		else
			slope = FixedDiv(y2f-y0f,x2f-x0f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y2,y0)
				local maxy = max(y2,y0)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y2*256 + FixedMul(slope,x2*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x2,x0)
				local maxx = max(x2,x0)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x2*256 , slope)/256 + y2
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		return
	end
	
	// Top triangle (topx, topy, height, lslope, rslope)
	local t = {}
	// Bottom Triangle (bottomx, bottomy, height, lslope, rslope)
	local b = {}
	// Sort the vertices and keep the order
	if (y0 == y1 or y0 == y2 or y1 == y2)
		local top = true
		if(y0 == y1 and y0 == y2)
			return
		elseif(y1 == y2)
			if(y0 > y1)
				top = false
			end
			t[0] = x0 // x
			t[1] = y0 // y
			t[2] = y1-y0
			if(x2-x0 != 0)
				t[3] = FixedDiv(y2f-y0f, x2f-x0f) // left slope
			end
			if(x1-x0 != 0)
				t[4] = FixedDiv(y1f-y0f, x1f-x0f) // right slope
			end
		elseif(y0 == y2)
			if(y1 > y2)
				top = false
			end
			t[0] = x1 // x
			t[1] = y1 // y
			t[2] = y2-y1
			if(x0-x1 != 0)
				t[3] = FixedDiv(y0f-y1f, x0f-x1f) // left slope
			end
			if(x2-x1 != 0)
				t[4] = FixedDiv(y2f-y1f, x2f-x1f) // right slope
			end
		else
			if(y2 > y0)
				top = false
			end
			t[0] = x2 // x
			t[1] = y2 // y
			t[2] = y0-y2
			if(x1-x2 != 0)
				t[3] = FixedDiv(y1f-y2f, x1f-x2f) // left slope
			end
			if(x0-x2 != 0)
				t[4] = FixedDiv(y0f-y2f, x0f-x2f) // right slope
			end
		end
		local starty
		local endy
		if(top)
			starty = t[1] // y-coordinate of the top of the triangle
			endy = t[1]+t[2] // starty + height
		else
			starty = t[1]+t[2]
			endy = t[1]
		end
		local startx
		local endx
		// Draw the triangle
		for i = starty, endy // i = starty, starty + height
			if(i > 319)
				return
			elseif i < 0
				if(endy < 0)
					return
				end
				i = 0
			end
			if(t[3] == nil)
				startx = t[0] // startx = topx
			else
				startx = FixedDiv(i*256- t[1]*256 + FixedMul(t[3],t[0]*256),t[3])/256
			end
			if(t[4] == nil)
				endx = t[0] // endx = topx
			else
				endx = FixedDiv(i*256- t[1]*256 + FixedMul(t[4],t[0]*256),t[4])/256
			end
			// Draw a line!
			v.drawFill(min(startx,endx), i, abs(endx-startx), 1, c)
		end
		return
	elseif(y0 < y1 and y0 < y2)
		t[0] = x0 // x
		t[1] = y0 // y
		if(x2-x0 != 0)
			t[3] = FixedDiv(y2f-y0f, x2f-x0f) // left slope
		end
		if(x1-x0 != 0)
			t[4] = FixedDiv(y1f-y0f, x1f-x0f) // right slope
		end
		if(y1 < y2)
			t[2] = y1 - y0 // height
			b[0] = x2 // x
			b[1] = y2 //y
			b[2] = y2 - y1 // height
			b[3] = t[3] // left slope
			if(x1-x2 != 0)
				b[4] = FixedDiv(y1f-y2f, x1f-x2f) // right slope
			end
		else
			t[2] = y2 - y0 // height
			b[0] = x1 // x
			b[1] = y1 //y
			b[2] = y1 - y2 // height
			if(x1-x2 != 0)
				b[3] = FixedDiv(y1f-y2f, x1f-x2f) // right slope
			end
			b[4] = t[4] // right slope
		end
	elseif(y1 < y0 and y1 < y2)
		t[0] = x1 // x
		t[1] = y1 // y
		if(x0-x1 != 0)
			t[3] = FixedDiv(y0f-y1f, x0f-x1f) // left slope
		end
		if(x2-x1 != 0)
			t[4] = FixedDiv(y2f-y1f, x2f-x1f) // right slope
		end
		if(y2 < y0)
			t[2] = y2 - y1 // height
			b[0] = x0 // x
			b[1] = y0 //y
			b[2] = y0 - y2 // height
			b[3] = t[3] // left slope
			if(x2-x0 != 0)
				b[4] = FixedDiv(y2f-y0f, x2f-x0f) // right slope
			end
		else
			t[2] = y0 - y1 // height
			b[0] = x2 // x
			b[1] = y2 //y
			b[2] = y2 - y0 // height
			if(x2-x0 != 0)
				b[3] = FixedDiv(y2f-y0f, x2f-x0f) // right slope
			end
			b[4] = t[4] // right slope
		end
	else
		t[0] = x2 // x
		t[1] = y2 // y
		if(x1-x2 != 0)
			t[3] = FixedDiv(y1f-y2f, x1f-x2f) // left slope
		end
		if(x0-x2 != 0)
			t[4] = FixedDiv(y0f-y2f, x0f-x2f) // right slope
		end
		if(y0 < y1)
			t[2] = y0 - y2 // height
			b[0] = x1 // x
			b[1] = y1 //y
			b[2] = y1 - y0 // height
			b[3] = t[3] // left slope
			if(x0-x1 != 0)
				b[4] = FixedDiv(y0f-y1f, x0f-x1f) // right slope
			end
		else
			t[2] = y1 - y2 // height
			b[0] = x0 // x
			b[1] = y0 //y
			b[2] = y0 - y1 // height
			if(x0-x1 != 0)
				b[3] = FixedDiv(y0f-y1f, x0f-x1f) // right slope
			end
			b[4] = t[4] // right slope
		end
	end
	//Draw the top triangle
	local startx
	local endx
	for i = t[1], t[1]+t[2] // i = topy, topy + height
		if(i < 0)
			i = -1
		elseif(i > 199)
			i = t[1]+t[2]
		end
		if(t[3] == nil)
			startx = t[0] // startx = topx
		else
			startx = FixedDiv(i*256- t[1]*256 + FixedMul(t[3],t[0]*256),t[3])/256
		end
		if(t[4] == nil)
			endx = t[0] // endx = topx
		else
			endx = FixedDiv(i*256- t[1]*256 + FixedMul(t[4],t[0]*256),t[4])/256
		end
		// Draw a line!
		v.drawFill(min(startx,endx), i, abs(endx-startx), 1, c)
	end
	for i = b[1]-b[2], b[1] // i = topy, topy + height
		if (i < 0)
			if(b[1] < 0)
				return
			end
			i = -1
		elseif (i > 199)
			return
		end
		if(b[3] == nil)
			startx = b[0] // startx = topx
		else
			startx = FixedDiv(i*256- b[1]*256 + FixedMul(b[3],b[0]*256),b[3])/256
		end
		if(b[4] == nil)
			endx = b[0] // endx = topx
		else
			endx = FixedDiv(i*256- b[1]*256 + FixedMul(b[4],b[0]*256),b[4])/256
		end
		// Draw a line!
		v.drawFill(min(startx,endx), i, abs(endx-startx), 1, c)
	end
end

// Draw a quad!
// Warning! The logic only works if every angle <= 180 degrees
local function DrawQuad2D(x0, y0, x1, y1, x2, y2, x3, y3, c, v, wireframe)
	local phases = 3
	if(y0 == y1)
		phases = $1 - 1
	end
	//if(y0 == y2)
	//	phases = $1 - 1
	//end
	if(y0 == y3)
		phases = $1 - 1
	end
	if(y1 == y2)
		phases = $1 - 1
	end
	//if(y1 == y3)
	//	phases = $1 - 1
	//end
	if(y2 == y3)
		phases = $1 - 1
	end
	if (phases < 1)
		return
	end
	local x0f = x0*FRACUNIT
	local y0f = y0*FRACUNIT
	local x1f = x1*FRACUNIT
	local y1f = y1*FRACUNIT
	local x2f = x2*FRACUNIT
	local y2f = y2*FRACUNIT
	local x3f = x3*FRACUNIT
	local y3f = y3*FRACUNIT
	local start0
	local start1
	local start2
	local h0 // Height of the fisrt phase
	local h1 // Second
	local h2 // Third
	local p0 = {} // First phase: point1 x, y, slope, point2 x, y, slope2
	local p1 = {} // Second
	local p2 = {} // Third
	
	if(wireframe)
		local slope
		local x
		local y
		if(x0 == x1)
			v.drawFill(x0,min(y0,y1),1,abs(y0-y1),c)
		elseif(y0 == y1)
			v.drawFill(min(x0,x1),y0,abs(x0-x1),1,c)
		else
			slope = FixedDiv(y0f-y1f,x0f-x1f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y0,y1)
				local maxy = max(y0,y1)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y0*256 + FixedMul(slope,x0*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x0,x1)
				local maxx = max(x0,x1)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x0*256 , slope)/256 + y0
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		if(x1 == x2)
			v.drawFill(x1,min(y1,y2),1,abs(y1-y2),c)
		elseif(y1 == y2)
			v.drawFill(min(x1,x2),y1,abs(x1-x2),1,c)
		else
			slope = FixedDiv(y1f-y2f,x1f-x2f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y1,y2)
				local maxy = max(y1,y2)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y1*256 + FixedMul(slope,x1*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x1,x2)
				local maxx = max(x1,x2)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x1*256 , slope)/256 + y1
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		if(x2 == x3)
			v.drawFill(x2,min(y2,y3),1,abs(y2-y3),c)
		elseif(y2 == y3)
			v.drawFill(min(x2,x3),y2,abs(x2-x3),1,c)
		else
			slope = FixedDiv(y2f-y3f,x2f-x3f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y2,y3)
				local maxy = max(y2,y3)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y2*256 + FixedMul(slope,x2*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x2,x3)
				local maxx = max(x2,x3)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x2*256 , slope)/256 + y2
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		if(x3 == x0)
			v.drawFill(x3,min(y3,y0),1,abs(y3-y0),c)
		elseif(y3 == y0)
			v.drawFill(min(x3,x0),y3,abs(x3-x0),1,c)
		else
			slope = FixedDiv(y3f-y0f,x3f-x0f)
			if(abs(slope) > FRACUNIT)
				local miny = min(y3,y0)
				local maxy = max(y3,y0)
				for i = miny,maxy
					if(i < 0)
						i = -1
					elseif(i > 199)
						i = maxy
					end
					x = FixedDiv(i*256- y3*256 + FixedMul(slope,x3*256),slope)/256
					v.drawFill(x,i,1,1,c)
				end
			else
				local minx = min(x3,x0)
				local maxx = max(x3,x0)
				for i = minx,maxx
					if(i < 0)
						i = -1
					elseif(i > 319)
						i = maxx
					end
					y = FixedMul(i*256 - x3*256 , slope)/256 + y3
					v.drawFill(i,y,1,1,c)
				end
			end
		end
		return
	end
	
	if (phases == 1)
		if(y0 == y1 and y0 == y2) // Check if we have a flat triangle
			p0[0] = x3
			p0[1] = y3
			p0[3] = x3
			p0[4] = y3
			if (y3 < y0) // Check if y3 is the top point
				start0 = y3
				h0 = y0 - y3
			else
				start0 = y0
				h0 = y3 - y0
			end
			if(x0 != x3)
				p0[2] = FixedDiv(y0f-y3f, x0f-x3f)
			end
			if(x2 != x3)
				p0[5] = FixedDiv(y2f-y3f, x2f-x3f)
			end
		elseif(y0 == y1 and y0 == y3)
			p0[0] = x2
			p0[1] = y2
			p0[3] = x2
			p0[4] = y2
			if (y2 < y0) // Check if y2 is the top point
				start0 = y2
				h0 = y0 - y2
			else
				start0 = y0
				h0 = y2 - y0
			end
			if(x1 != x2)
				p0[2] = FixedDiv(y1f-y2f, x1f-x2f)
			end
			if(x2 != x3)
				p0[5] = FixedDiv(y2f-y3f, x2f-x3f)
			end
		elseif(y0 == y2 and y0 == y3)
			p0[0] = x1
			p0[1] = y1
			p0[3] = x1
			p0[4] = y1
			if (y1 < y0) // Check if y1 is the top point
				start0 = y1
				h0 = y0 - y1
			else
				start0 = y0
				h0 = y1 - y0
			end
			if(x0 != x1)
				p0[2] = FixedDiv(y0f-y1f, x0f-x1f)
			end
			if(x2 != x1)
				p0[5] = FixedDiv(y2f-y1f, x2f-x1f)
			end
		elseif(y1 == y2 and y1 == y3)
			p0[0] = x0
			p0[1] = y0
			p0[3] = x0
			p0[4] = y0
			if (y0 < y1) // Check if y0 is the top point
				start0 = y0
				h0 = y1 - y0
			else
				start0 = y1
				h0 = y0 - y1
			end
			if(x1 != x0)
				p0[2] = FixedDiv(y1f-y0f, x1f-x0f)
			end
			if(x3 != x0)
				p0[5] = FixedDiv(y0f-y3f, x0f-x3f)
			end
		else // Now we know there are flat lines in top and bottom
			if(y0 < y1)
				p0[0] = x0
				p0[1] = y0
				p0[3] = x3
				p0[4] = y3
				start0 = y0
				h0 = y1 - y0
				if(x1 != x0)
					p0[2] = FixedDiv(y1f-y0f, x1f-x0f)
				end
				if(x2 != x3)
					p0[5] = FixedDiv(y2f-y3f, x2f-x3f)
				end
			elseif(y1 < y2)
				p0[0] = x0
				p0[1] = y0
				p0[3] = x1
				p0[4] = y1
				start0 = y1
				h0 = y2 - y1
				if(x0 != x3)
					p0[2] = FixedDiv(y3f-y0f, x3f-x0f)
				end
				if(x2 != x1)
					p0[5] = FixedDiv(y2f-y1f, x2f-x1f)
				end
			elseif(y2 < y3)
				p0[0] = x2
				p0[1] = y2
				p0[3] = x1
				p0[4] = y1
				start0 = y2
				h0 = y3 - y2
				if(x2 != x3)
					p0[2] = FixedDiv(y2f-y3f, x2f-x3f)
				end
				if(x1 != x0)
					p0[5] = FixedDiv(y1f-y0f, x1f-x0f)
				end
			else // y3 < y1
				p0[0] = x2
				p0[1] = y2
				p0[3] = x3
				p0[4] = y3
				start0 = y3
				h0 = y0 - y3
				if(x2 != x1)
					p0[2] = FixedDiv(y2f-y1f, x2f-x1f)
