			endx = FixedDiv(i*256- p2[4]*256 + FixedMul(p2[5],p2[3]*256),p2[5])/256
		end
		// Draw a line!
		v.drawFill(min(startx,endx), i, abs(endx-startx), 1, c)
	end
end

local function InitModels(models)
	local pal = server.colorlist
	// Init a null model
	models[MOD_NULL] = {}
	models[MOD_NULL][0] = {}
	models[MOD_NULL][1] = {}
	//init a cube
	models[MOD_CUBE] = {}
	models[MOD_CUBE][0] = {}
	models[MOD_CUBE][1] = {}
	
	local cv = models[MOD_CUBE][0] // Vertex is a xyz-vector
	local cf = models[MOD_CUBE][1]  // Face contains three vertices and a color
	
	local r = 75*FRACUNIT
	
	cv[0] = Vec3(-r,-r,-r)
	cv[1] = Vec3(-r,-r, r)
	cv[2] = Vec3(-r, r,-r)
	cv[3] = Vec3(-r, r, r)
	cv[4] = Vec3( r,-r,-r)
	cv[5] = Vec3( r,-r, r)
	cv[6] = Vec3( r, r,-r)
	cv[7] = Vec3( r, r, r)
	
	// face contains (bool quad, v1, v2, v3[, v4], r, g, b)
	// Use v4 only in quads
	
	cf[0] = Vec8(true, 0, 1, 3, 2, 31, 0,  0)
	cf[1] = Vec8(true, 4, 6, 7, 5, 0,  31, 31)
	cf[2] = Vec8(true, 0, 2, 6, 4, 0,  31, 0)
	cf[3] = Vec8(true, 1, 5, 7, 3, 31, 0,  31)
	cf[4] = Vec8(true, 0, 4, 5, 1, 0,  0,  31)
	cf[5] = Vec8(true, 2, 3, 7, 6, 31, 31, 0)
	
	models[MOD_SPHERE] = {}
	models[MOD_SPHERE][0] = {}
	models[MOD_SPHERE][1] = {}
	
	local sv = models[MOD_SPHERE][0]
	local sf = models[MOD_SPHERE][1]
	
	// Create amiga ball
	local radius = 200*FRACUNIT
	local layers = 8 // Faces vertically
	local squares = 16 // Faces horizontally
	
	sv[0] = Vec3(0,radius,0)
	for i = 0, layers - 2
		local isin = sin(FixedMul(ANGLE_180, FixedDiv((i+1)*FRACUNIT,layers*FRACUNIT)))
		local icos = cos(FixedMul(ANGLE_180, FixedDiv((i+1)*FRACUNIT,layers*FRACUNIT)))
		for j = 1, squares
			local phase = FixedDiv(j*FRACUNIT, squares*FRACUNIT)
			local x = FixedMul(isin,cos(FixedMul(phase*2,ANGLE_180)))
			local z = FixedMul(isin,sin(FixedMul(phase*2,ANGLE_180)))
			sv[i*squares+j] = Vec3(FixedMul(x,radius), FixedMul(radius,icos), FixedMul(z,radius))
		end
	end
	sv[squares*(layers - 1) + 1] = Vec3(0,-radius,0)
	
	for i = 0, layers - 1
		for j = 0, squares - 1
			local white = false// Color. Either white or red
			if((i+j)%2 == 1)
				white = true
			end
			if (i == 0 or i == layers - 1) // Face is a triangle
				local v0
				local v1
				local v2
				if (i == 0)
					v2 = 0
					v1 = (j+1)%squares + 1
					v0 = j + 1
					if(white)
						sf[j] = Vec8(false, v0, v1, v2, 31, 31, 31)
					else
						sf[j] = Vec8(false, v0, v1, v2, 31, 0, 0)
					end
				else
					v0 = (i-1)*squares + j + 1
					v1 = squares*(layers - 1) + 1
					v2 = (i-1)*squares + (j+1)%squares + 1
					if(white)
						sf[j + i*squares] = Vec8(false, v0, v1, v2, 31, 31, 31)
					else
						sf[j + i*squares] = Vec8(false, v0, v1, v2, 31, 0, 0)
					end
				end
			else // Face is a quad
				local v1 = (i-1)*squares + j + 1
				local v0 = (i-1)*squares + (j+1)%squares + 1
				local v3 = i*squares + (j+1)%squares +1
				local v2 = i*squares + j + 1
				if(white)
					sf[j + i*squares] = Vec8(true, v0, v1, v2, v3, 31, 31, 31)
				else
					sf[j + i*squares] = Vec8(true, v0, v1, v2, v3, 31, 0, 0)
				end
			end
		end
	end
end

// Rotation matrix
local function RotationMatrix(angle, aiming)
	// Rotate the stuff
	local sinaiming = sin(aiming)
	local cosaiming = cos(aiming)
	local sinangle = sin(angle)
	local cosangle = cos(angle)
	
	local rot = {}
	rot[0] = {}
	rot[1] = {}
	rot[2] = {}
	rot[3] = {}
	
	rot[0][0] = cosangle
	rot[0][1] = 0
	rot[0][2] = sinangle
	rot[1][0] = FixedMul(sinangle, sinaiming)
	rot[1][1] = cosaiming
	rot[1][2] = -FixedMul(cosangle, sinaiming)
	rot[2][0] = -FixedMul(sinangle, cosaiming)
	rot[2][1] = sinaiming
	rot[2][2] = FixedMul(cosangle, cosaiming)
	
/*	rot[0][0] = cosangle
	rot[0][1] = 0
	rot[0][2] = sinangle
	rot[1][0] = 0
	rot[1][1] = FRACUNIT
	rot[1][2] = 0
	rot[2][0] = -sinangle
	rot[2][1] = 0
	rot[2][2] = cosangle*/
	
	rot[0][3] = 0
	rot[1][3] = 0
	rot[2][3] = 0
	rot[3][0] = 0
	rot[3][1] = 0
	rot[3][2] = 0
	rot[3][3] = FRACUNIT
		
	return rot
end

// Number of objects
local num_objects = 0

//Spawn an object
local function SpawnObject(x, y, z, angle, modelnum, patchname)
	local object = {}
	object[0] = x
	object[1] = y
	object[2] = z
	object[3] = 0 // Aiming
	object[4] = angle
	object[5] = 0 // Roll
	if(modelnum < SPRITESTART) // Let's assume we won't have more than 256 models
		object[6] = ST_MODEL // Sprite type is model
		object[7] = server.models[modelnum]
	else
		modelnum = $1 - SPRITESTART
		object[6] = ST_SPRITE // Sprite type is sprite
		object[7] = patchname // The sprite's name
		object[8] = 0 // Flags
		object[9] = 0 // Colormap
	end
	server.objects[num_objects] = object
	num_objects = $1 + 1
	return object
end

// Reload the game map
local function ResetMap()
	server.objects = {}
	num_objects = 0
	SpawnObject(0, 0, 0, 0, MOD_SPHERE)
	for i = 0, 7
		for j = 0, 2
			local obj0 = SpawnObject(FixedMul(cos(ANGLE_45*i), 800*FRACUNIT), j*250*FRACUNIT-250*FRACUNIT, FixedMul(sin(ANGLE_45*i), 800*FRACUNIT), ANGLE_45*i, MOD_CUBE)
			obj0[3] = i*ANGLE_45
		end
	end
end

local function Init(p)
	p.inited = true
	CONS_Printf(p, "Initializing Lua Graphics..")
	p.luax = 0
	p.luay = 0
	p.luaz = -1200*FRACUNIT
	p.mo.angle = 0
	p.aiming = 0
	if(p == server)
		p.colors = {}
		p.colorlist = {}
		InitColors(p.colorlist)
		p.models = {}
		InitModels(p.models)
		ResetMap()
	end
end

local function CompareDepth(a,b)
	return a[1] > b[1]
end


//The main Drawing function
local function DrawScene(v, p)
	// Use RGB-values from palette by pal[r][g][b]
	local pal = server.colorlist
	// Rotate the world
	local view = RotationMatrix(-p.mo.angle,p.aiming)
	local x = {} // Vertex x
	local y = {} // Vertex y
	local z = {} // Vertex z
	local modx = {}
	local mody = {}
	local modz = {}
	local newx = {}
	local newy = {}
	local newz = {}
	local drawx = {}
	local drawy = {}
	local det
	local models = server.models
	local ox // Sprite/Object x
	local oy // Sprite/Object y
	local oz // Sprite/Object z
	local newox 
	local newoy
	local newoz
	local drawox
	local drawoy
	
	local wireframe = false
	
	if(leveltime%(TICRATE*2) > TICRATE*5/3)
		wireframe = true
	end
	
	local drawlist = {}// List of objects organized by depth
	local vertexlist = {}
	
	// Draw a background
	v.drawFill(0,0,320,200,pal[0][0][0])
	DrawTriangle2D(0,0, 320,200, 320,0, pal[abs(sin(leveltime*25000000))*16/FRACUNIT][0][0], v, false)
	DrawTriangle2D(0,0, 0,200, 320,200, pal[0][0][abs(sin(leveltime*35000000))*16/FRACUNIT], v, false)
	DrawTriangle2D(0,200, 320,200, 160,0, pal[0][abs(sin(leveltime*15000000))*16/FRACUNIT][0], v, false)
	
	// Organize models and sprites by depth
	for i = 0, #server.objects
		ox = server.objects[i][0] - p.luax
		oy = server.objects[i][1] - p.luay
		oz = server.objects[i][2] - p.luaz
		newoz = FixedMul(ox,view[2][0]) + FixedMul(oy,view[2][1]) + FixedMul(oz,view[2][2])
		if(newoz > 0) // Draw only if the thing is forward
			local object = {}
			object[0] = server.objects[i]
			object[1] = newoz
			// The sorting function wants the list to start from ONE
			drawlist[#drawlist + 1] = object
		end
	end
	// Make the players visible in the draw-list
	for p2 in players.iterate
		if (#p != #p2 and p2.inited)
			ox = p2.luax - p.luax
			oy = p2.luay - p.luay
			oz = p2.luaz - p.luaz
			newoz = FixedMul(ox,view[2][0]) + FixedMul(oy,view[2][1]) + FixedMul(oz,view[2][2])
			if(newoz > 0)
				local obj = {}
				// Object data
				obj[0] = {}
				
				obj[0][0] = p2.luax
				obj[0][1] = p2.luay
				obj[0][2] = p2.luaz
				obj[0][6] = ST_SPRITE
				obj[0][7] = v.cachePatch(skins[p2.mo.skin].face)
				obj[0][8] = 0
				obj[0][9] = v.getColormap(p2.mo.skin, p2.mo.color)
				//Distance
				obj[1] = newoz
				
				drawlist[#drawlist + 1] = obj
			end
		end
	end
	local forward
	// Organize and draw everything from the drawlist!
	if (drawlist[1] != nil)
		table.sort(drawlist, CompareDepth)
		for obj = 1, #drawlist
			// Model's rotation matrix
			if(drawlist[obj][0][6] == ST_MODEL)
				local modm = RotationMatrix(drawlist[obj][0][4], drawlist[obj][0][3])
				local model = drawlist[obj][0][7]
				for i = 0, #model[0]
					vertexlist[i] = {}
					x[i] = model[0][i][0]
					y[i] = model[0][i][1]
					z[i] = model[0][i][2]
					
					// Apply the model's rotation matrix and movements and camera movement
					modx[i] = FixedMul(x[i],modm[0][0]) + FixedMul(y[i],modm[0][1]) + FixedMul(z[i],modm[0][2]) + drawlist[obj][0][0] - p.luax
					mody[i] = FixedMul(x[i],modm[1][0]) + FixedMul(y[i],modm[1][1]) + FixedMul(z[i],modm[1][2]) + drawlist[obj][0][1] - p.luay
					modz[i] = FixedMul(x[i],modm[2][0]) + FixedMul(y[i],modm[2][1]) + FixedMul(z[i],modm[2][2]) + drawlist[obj][0][2] - p.luaz

					// Use camera's rotation matrix
					newz[i] = FixedMul(modx[i],view[2][0]) + FixedMul(mody[i],view[2][1]) + FixedMul(modz[i],view[2][2])
					if(newz[i] < 1*FRACUNIT) // Don't draw things that are too close
						vertexlist[i][2] = false
					else
						newx[i] = FixedMul(modx[i],view[0][0]) + FixedMul(mody[i],view[0][1]) + FixedMul(modz[i],view[0][2])
						newy[i] = FixedMul(modx[i],view[1][0]) + FixedMul(mody[i],view[1][1]) + FixedMul(modz[i],view[1][2])
						newx[i] = FixedDiv($1,-newz[i])
						newy[i] = FixedDiv($1,-newz[i])
						vertexlist[i][0] = newx[i] / 205 + 160
						vertexlist[i][1] = newy[i] / 205 + 100
						vertexlist[i][2] = true // True means the point can be drawn
					end
				end
				for face = 0, #model[1]
					forward = true
					local outoffov = true
					local vertices = 3
					local quad = model[1][face][0]
					if (quad)
						vertices = 4
					end
					for i = 0, vertices -1 // Matrices for each vertex
						local vert = model[1][face][i+1]
						drawx[i] = vertexlist[vert][0]
						drawy[i] = vertexlist[vert][1]
						if(vertexlist[vert][2] == false)
							forward = false
						elseif(drawx[i] >= 0 and drawx[i] < 320 and drawy[i] >= 0 and drawy[i] < 200)
							outoffov = false
						end
					end
					if(wireframe and forward and not outoffov)
						if (quad)
							DrawQuad2D(drawx[0],drawy[0], drawx[1],drawy[1], drawx[2],drawy[2], drawx[3], drawy[3], pal[31][31][31], v, wireframe)
						else
							DrawTriangle2D(drawx[0],drawy[0], drawx[1],drawy[1], drawx[2],drawy[2], pal[31][31][31], v, wireframe)
						end
					elseif(forward and not outoffov)
						if(quad)
							local det = drawx[0]*drawy[1] - drawx[1]*drawy[0] + drawx[1]*drawy[2] - drawx[2]*drawy[1] + drawx[2]*drawy[3] - drawx[3]*drawy[2] + drawx[3]*drawy[0] - drawx[0]*drawy[3]
							if (det < 0)
								// The modified color of the face
								local r
								local g
								local b
								r = abs(FixedMul(model[1][face][5]*FRACUNIT, sin(leveltime*10000000))/FRACUNIT)
								g = abs(FixedMul(model[1][face][6]*FRACUNIT, sin(leveltime*10000000+ANG60*2))/FRACUNIT)
								b = abs(FixedMul(model[1][face][7]*FRACUNIT, sin(leveltime*10000000-ANG60*2))/FRACUNIT)
								local color = pal[r][g][b]
								DrawQuad2D(drawx[0],drawy[0], drawx[1],drawy[1], drawx[2],drawy[2], drawx[3], drawy[3], color, v, wireframe)
							end
							//for i = 0, 3 // Numbers for debugging
							//	v.drawString(drawx[i],drawy[i],i)
							//end
						else
							local det = drawx[0]*drawy[1] - drawx[1]*drawy[0] + drawx[1]*drawy[2] - drawx[2]*drawy[1] + drawx[2]*drawy[0] - drawx[0]*drawy[2]
							if (det < 0)
								// The modified color of the face
								local r
								local g
								local b
								r = abs(FixedMul(model[1][face][4]*FRACUNIT, sin(leveltime*10000000))/FRACUNIT)
								g = abs(FixedMul(model[1][face][5]*FRACUNIT, sin(leveltime*10000000+ANG60*2))/FRACUNIT)
								b = abs(FixedMul(model[1][face][6]*FRACUNIT, sin(leveltime*10000000-ANG60*2))/FRACUNIT)
								local color = pal[r][g][b]
								DrawTriangle2D(drawx[0],drawy[0], drawx[1],drawy[1], drawx[2],drawy[2], color, v, wireframe)
							end
						end
					end
				end
			else
				ox = drawlist[obj][0][0] - p.luax
				oy = drawlist[obj][0][1] - p.luay
				oz = drawlist[obj][0][2] - p.luaz
				// Use camera's rotation matrix
				newox = FixedMul(ox,view[0][0]) + FixedMul(oy,view[0][1]) + FixedMul(oz,view[0][2])
				newoy = FixedMul(ox,view[1][0]) + FixedMul(oy,view[1][1]) + FixedMul(oz,view[1][2])
				newoz = FixedMul(ox,view[2][0]) + FixedMul(oy,view[2][1]) + FixedMul(oz,view[2][2])
				
				local patch = drawlist[obj][0][7]
				local flags = drawlist[obj][0][8]
				local colormap = drawlist[obj][0][9]
				
				drawox = FixedDiv(newox,-newoz)*320 + 160 * FRACUNIT
				drawoy = FixedDiv(newoy,-newoz)*320 + 100 * FRACUNIT
				
				v.drawScaled(drawox, drawoy, FixedDiv(320*FRACUNIT,newoz), patch, flags, colormap)
			end
		end
	end
end

