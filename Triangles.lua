addHook("ThinkFrame", function()
	if(gamemap > 1)
		return
	end
	for p in players.iterate
		if not (p.inited) and p.jointime > TICRATE
			Init(p)
		end
		if not (p.inited)
			return
		end
		// Resetting players
		// And reducing trouble and sounds
		p.mo.momx = 0
		p.mo.momy = 0
		p.mo.momz = 0
		if not (p.pflags & PF_JUMPSTASIS)
			p.pflags = $1 + PF_JUMPSTASIS
		end
		if(p.pflags & PF_STARTDASH)
			p.pflags = $1 - PF_STARTDASH
		end
		// Move the player
		local view = RotationMatrix(p.mo.angle,0)
		local dz = p.cmd.forwardmove*32768
		local dx = -p.cmd.sidemove*32768
		if (p.cmd.buttons & BT_USE)
			p.luay = $1 - 25*FRACUNIT
		end
		if (p.cmd.buttons & BT_JUMP)
			p.luay = $1 + 25*FRACUNIT
		end
		p.luax = $1 + FixedMul(dx,view[0][0]) + FixedMul(dz,view[0][2])
		p.luaz = $1 + FixedMul(dx,view[2][0]) + FixedMul(dz,view[2][2])
	end
	local rot = RotationMatrix(ANG1/2,0)
	for i = 0, #server.objects
		local ox = server.objects[i][0] // Original x
		local oz = server.objects[i][2] // Original z
		server.objects[i][0] = FixedMul(ox,rot[0][0]) + FixedMul(oz,rot[0][2])
		server.objects[i][2] = FixedMul(ox,rot[2][0]) + FixedMul(oz,rot[2][2])
		server.objects[i][3] = $1 + ANG1
		server.objects[i][4] = $1 + ANG2
	end
end)

